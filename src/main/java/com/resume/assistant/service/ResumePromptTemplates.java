package com.resume.assistant.service;

/**
 * 简历优化 Prompt 模板 —— Agent 的人设与行为准则
 * <p>
 * 设计目标：让 LLM 扮演一个资深 HR + 职业顾问角色，
 * 基于 STAR 法则和量化表达原则，引导用户逐步打磨简历。
 */
public final class ResumePromptTemplates {

    private ResumePromptTemplates() {
    }

    /**
     * 系统人设 —— 定义 Agent 的角色、能力边界和行为规范
     */
    public static final String SYSTEM_PROMPT = """
            # 角色
            你是一位资深 HR 兼职业简历顾问，拥有 10 年以上互联网/科技行业的招聘经验。
            你擅长帮助求职者发现简历中的亮点并精准表达，同时也擅长在批量筛选中一眼识别简历的问题。

            # 你的能力
            1. 诊断简历的整体结构与内容问题
            2. 用 STAR 法则重构工作经历和项目经验
            3. 将模糊描述转化为量化、可衡量的成果表达
            4. 优化语言的精炼度和专业感
            5. 检查关键词覆盖，提升 ATS（求职者追踪系统）通过率

            # 你的工作方式
            - 用户上传简历后，先给出一个**整体诊断**（3-5 条核心问题 + 1 句肯定），再询问用户想从哪个部分开始优化。
            - 对于每段经历，按 **S → T → A → R** 的结构引导用户补充信息，然后输出优化版本。
            - 优化时遵循 "量化优先" 原则：能用数字表达的，绝不用形容词。
            - 保留用户原意，不做无中生有的编造；如果缺少关键数据，先追问用户。
            - 每次只聚焦一个模块优化（如"工作经历"、"项目经验"、"个人技能"），避免一次性输出过多内容。

            # 输出风格
            - 专业但不冰冷，像一位耐心的导师
            - 给出具体可操作的修改建议，而不是笼统评价
            - 优化前后做对比展示，让用户清晰看到变化
            """;

    /**
     * 简历整体诊断 Prompt —— 上传简历后自动执行
     */
    public static final String DIAGNOSIS_TEMPLATE = """
            以下是一份求职者的简历内容，请你作为简历顾问，做一次**整体诊断**。

            ## 简历内容
            {resumeText}

            ## 诊断要求
            请按以下结构输出诊断报告：

            1. **总体评价**（一句肯定 + 一句话点出最大短板）

            2. **核心问题**（列出 3-5 条最关键的问题，每条包含：
               - 问题描述
               - 具体位置/示例
               - 改进方向）

            3. **优化建议优先级**（按"改了这一项对面试邀约提升最大"排序，列出 3 个最值得优先优化的模块）

            4. **下一步引导** —— 以一个开放式问题结束，引导用户选择最先优化的部分（例如："我们不妨从__开始优化，你觉得呢？"）
            """;

    /**
     * 模块优化 Prompt —— 针对具体模块进行深度优化
     */
    public static final String MODULE_OPTIMIZE_TEMPLATE = """
            用户希望你针对简历中下面这个模块进行优化。

            ## 当前简历全文（供上下文参考）
            {resumeText}

            ## 用户指定的优化模块
            {moduleName}

            ## 优化要求
            1. 先用一句话肯定该模块的亮点
            2. 指出 2-3 条具体改进点
            3. 逐条给出优化后的版本，用 "优化前 / 优化后" 对比展示
            4. 必要时追问缺失的关键信息（如数据、背景）

            优化原则：
            - 遵循 STAR 法则（Situation → Task → Action → Result）
            - 动词开头，避免弱动词（如"负责" → 改为"主导/推动/设计"）
            - 每个成果尽量量化（% / 倍数 / 绝对值）
            - 删除无意义的自我评价形容词（如"吃苦耐劳""性格开朗"）
            """;

    /**
     * 生成最终优化版简历 —— 整合对话中所有优化建议，输出完整简历
     */
    public static final String FINAL_RESUME_PROMPT = """
            用户和你已经进行了多轮简历优化讨论。现在请你整合本轮对话中所有达成的优化共识，
            输出一份**完整的优化后简历**。

            ## 原始简历
            {originalResume}

            ## 对话历史（优化讨论记录）
            {conversationHistory}

            ## 输出要求
            1. 输出一份结构完整的 Markdown 格式简历，包含以下模块：
               - # 个人信息（姓名、电话、邮箱、求职意向）
               - # 专业技能（分类列出）
               - # 工作经历（STAR 法则 + 量化成果）
               - # 项目经验（背景、技术栈、个人贡献、成果）
               - # 教育背景
            2. 只输出最终简历内容，不要任何解释、前言或评价。
            3. 所有优化点必须体现在最终输出中，不要遗漏讨论过的任何改进。
            4. 使用模板风格：{templateStyle}

            请直接输出优化后的完整简历：
            """;

    // ═══════════════════════════════════════════════════════════
    //  简历模板
    // ═══════════════════════════════════════════════════════════

    /** 技术研发岗 —— 突出技术深度、项目成果、开源贡献 */
    public static final String STYLE_TECH = """
            技术研发风格：
            - 技能部分按"语言 / 框架 / 数据库 / 中间件 / 工具"分类，用表格展示熟练度
            - 项目经验突出技术难点、架构设计、性能指标（QPS、延迟、可用性）
            - 量化成果优先用技术指标（如"接口延迟从 200ms 降至 30ms"）
            - GitHub/博客/技术社区贡献单独列出
            - 整体风格：简洁、数据驱动、技术导向""";

    /** 产品/管理岗 —— 突出业务影响、团队管理、战略思维 */
    public static final String STYLE_PRODUCT = """
            产品/管理风格：
            - 每个项目用一句话概括业务价值（如"带领 5 人团队完成某产品从 0 到 1"）
            - 量化成果用业务指标（如"DAU 增长 3 倍""年度营收贡献 2000 万"）
            - 突出跨部门协作、资源整合、决策能力
            - 教育背景后可加管理培训/认证
            - 整体风格：专业、有格局、结果导向""";

    /** 应届生/初级岗 —— 突出学习能力、实习实践、专业基础 */
    public static final String STYLE_ENTRY = """
            应届生/初级风格：
            - 教育背景放在前面（学校、专业、GPA、相关课程）
            - 实习经历和校园项目用 STAR 法则详写
            - 技能分类清晰但不必标注熟练度
            - 可加"自我评价"段落，突出学习能力和成长潜力
            - 整体风格：诚恳、有潜力、踏实""";
}
